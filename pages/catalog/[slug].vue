<script setup>
import { ref, computed, watch, defineAsyncComponent, onMounted, onBeforeUnmount } from 'vue'
import { categories, allComponents, slugOf, findBySlug, docUrl } from '~/components/catalog/registry'

const route = useRoute()
const slug = computed(() => String(route.params.slug || ''))
const entry = computed(() => findBySlug(slug.value))

// Live demo component + its raw source (kept in sync automatically:
// the same .vue file is both rendered and displayed as code)
const demoLoaders = import.meta.glob('~/components/catalog/demos/*.vue')
const sourceLoaders = import.meta.glob('~/components/catalog/demos/*.vue', { query: '?raw', import: 'default' })
const demoKey = name => Object.keys(demoLoaders).find(k => k.endsWith(`/${name}.vue`))

const demo = computed(() => {
  const key = entry.value && demoKey(entry.value.name)
  return key ? defineAsyncComponent(demoLoaders[key]) : null
})

const source = ref('')
watch(entry, async (e) => {
  const key = e && demoKey(e.name)
  source.value = key ? await sourceLoaders[key]() : ''
}, { immediate: true })

// Extracted API docs (props / events / slots), generated by
// scripts/generate-catalog-api.mjs from primevue's index.d.ts files
const apiLoaders = import.meta.glob('~/components/catalog/api/*.json', { import: 'default' })
const { data: api } = await useAsyncData('catalog-api', async () => {
  const key = Object.keys(apiLoaders).find(k => k.endsWith(`/${slug.value}.json`))
  return key ? await apiLoaders[key]() : null
}, { watch: [slug], default: () => null })
const visibleProps = computed(() => (api.value?.props || []).filter(p => !p.deprecated))
const visibleEvents = computed(() => (api.value?.events || []).filter(e => !e.deprecated))
const visibleSlots = computed(() => (api.value?.slots || []).filter(s => !s.deprecated))

// Prev / next component navigation
const index = computed(() => allComponents.findIndex(c => slugOf(c.name) === slug.value))
const prev = computed(() => allComponents[index.value - 1])
const next = computed(() => allComponents[index.value + 1])

const query = ref('')
const filtered = computed(() => {
  const q = query.value.trim().toLowerCase()
  if (!q) return categories
  return categories
    .map(c => ({ ...c, items: c.items.filter(i => i.toLowerCase().includes(q)) }))
    .filter(c => c.items.length)
})

const showCode = ref(false)
const copied = ref(false)
const copyCode = async () => {
  await navigator.clipboard.writeText(source.value)
  copied.value = true
  setTimeout(() => { copied.value = false }, 1500)
}

const main = ref(null)
watch(slug, () => {
  showCode.value = false
  main.value?.scrollTo({ top: 0 })
})

const dark = useState('catalog-dark', () => false)
watch(dark, v => document.documentElement.classList.toggle('dark', v))
onMounted(() => document.documentElement.classList.toggle('dark', dark.value))
// The editor page styles itself for dark UI without the PrimeVue dark selector,
// so the class must not leak out of the catalog.
onBeforeUnmount(() => document.documentElement.classList.remove('dark'))
</script>

<template>
  <div class="catalog" :class="{ 'catalog-dark': dark }">
    <Toast />

    <header class="catalog-bar">
      <div class="bar-left">
        <NuxtLink to="/" class="back-link"><i class="pi pi-arrow-left" /> Editor</NuxtLink>
        <div class="bar-title">
          <span class="bar-logo">F</span>
          PrimeVue Catalog
          <span class="bar-count">{{ allComponents.length }} components</span>
        </div>
      </div>
      <div class="bar-right">
        <a class="bar-link" href="https://primevue.org" target="_blank" rel="noopener">primevue.org <i class="pi pi-external-link" /></a>
        <Button
          :icon="dark ? 'pi pi-sun' : 'pi pi-moon'" severity="secondary" text rounded
          :aria-label="dark ? 'Switch to light mode' : 'Switch to dark mode'" @click="dark = !dark"
        />
      </div>
    </header>

    <div class="catalog-body">
      <aside class="catalog-nav">
        <IconField class="nav-search">
          <InputIcon class="pi pi-search" />
          <InputText v-model="query" placeholder="Search components" fluid size="small" />
        </IconField>
        <nav>
          <div v-for="cat in filtered" :key="cat.label" class="nav-group">
            <div class="nav-cat">{{ cat.label }}</div>
            <NuxtLink
              v-for="item in cat.items" :key="item"
              :to="`/catalog/${slugOf(item)}`" class="nav-item"
              :class="{ active: slugOf(item) === slug }"
            >
              {{ item }}
            </NuxtLink>
          </div>
          <p v-if="!filtered.length" class="nav-empty">No matches for “{{ query }}”</p>
        </nav>
      </aside>

      <main ref="main" class="catalog-main">
        <template v-if="entry">
          <header class="comp-header">
            <div class="comp-heading">
              <span class="comp-cat">{{ entry.category }}</span>
              <h1 class="comp-name">{{ entry.name }}</h1>
            </div>
            <a class="comp-doclink" :href="docUrl(entry.name)" target="_blank" rel="noopener">
              <i class="pi pi-external-link" /> primevue.org/{{ slug }}
            </a>
          </header>
          <p v-if="api?.description" class="comp-desc">{{ api.description }}</p>

          <section class="preview-wrap">
            <div class="preview">
              <component :is="demo" v-if="demo" />
              <p v-else class="preview-missing">No demo available for this component.</p>
            </div>
            <div class="preview-actions">
              <button class="ghost-btn" @click="showCode = !showCode">
                <i class="pi pi-code" /> {{ showCode ? 'Hide code' : 'Show code' }}
              </button>
              <button v-if="showCode" class="ghost-btn" @click="copyCode">
                <i :class="copied ? 'pi pi-check' : 'pi pi-copy'" /> {{ copied ? 'Copied' : 'Copy' }}
              </button>
            </div>
            <pre v-if="showCode" class="code-block"><code>{{ source }}</code></pre>
          </section>

          <template v-if="api">
            <section v-if="visibleProps.length" class="api-section">
              <h2>Props <span class="api-count">{{ visibleProps.length }}</span></h2>
              <p v-if="api.extends" class="api-extends">Also accepts everything from <code>{{ api.extends }}</code>.</p>
              <div class="table-scroll">
                <table class="api-table">
                  <thead>
                    <tr><th>Name</th><th>Type</th><th>Default</th><th>Description</th></tr>
                  </thead>
                  <tbody>
                    <tr v-for="p in visibleProps" :key="p.name">
                      <td class="cell-name">{{ p.name }}</td>
                      <td class="cell-type"><code>{{ p.type }}</code></td>
                      <td class="cell-default"><code v-if="p.default !== undefined">{{ p.default }}</code><span v-else>—</span></td>
                      <td class="cell-desc">{{ p.description }}</td>
                    </tr>
                  </tbody>
                </table>
              </div>
            </section>

            <section v-if="visibleEvents.length" class="api-section">
              <h2>Events <span class="api-count">{{ visibleEvents.length }}</span></h2>
              <div class="table-scroll">
                <table class="api-table">
                  <thead>
                    <tr><th>Name</th><th>Signature</th><th>Description</th></tr>
                  </thead>
                  <tbody>
                    <tr v-for="e in visibleEvents" :key="e.name">
                      <td class="cell-name">{{ e.name }}</td>
                      <td class="cell-type"><code>{{ e.type }}</code></td>
                      <td class="cell-desc">{{ e.description }}</td>
                    </tr>
                  </tbody>
                </table>
              </div>
            </section>

            <section v-if="visibleSlots.length" class="api-section">
              <h2>Slots <span class="api-count">{{ visibleSlots.length }}</span></h2>
              <div class="table-scroll">
                <table class="api-table">
                  <thead>
                    <tr><th>Name</th><th>Description</th></tr>
                  </thead>
                  <tbody>
                    <tr v-for="s in visibleSlots" :key="s.name">
                      <td class="cell-name">{{ s.name }}</td>
                      <td class="cell-desc">{{ s.description }}</td>
                    </tr>
                  </tbody>
                </table>
              </div>
            </section>
          </template>

          <nav class="pager">
            <NuxtLink v-if="prev" :to="`/catalog/${slugOf(prev.name)}`" class="pager-link">
              <i class="pi pi-arrow-left" />
              <span><small>{{ prev.category }}</small>{{ prev.name }}</span>
            </NuxtLink>
            <span v-else />
            <NuxtLink v-if="next" :to="`/catalog/${slugOf(next.name)}`" class="pager-link pager-next">
              <span><small>{{ next.category }}</small>{{ next.name }}</span>
              <i class="pi pi-arrow-right" />
            </NuxtLink>
          </nav>

          <footer class="cat-footer">
            Editor and Chart are not in the catalog — they need the extra <code>quill</code> and <code>chart.js</code> dependencies.
          </footer>
        </template>

        <div v-else class="not-found">
          <h1>Component not found</h1>
          <p>No component named “{{ slug }}” in the catalog.</p>
          <NuxtLink to="/catalog/button" class="ghost-btn">Go to Button</NuxtLink>
        </div>
      </main>
    </div>
  </div>
</template>

